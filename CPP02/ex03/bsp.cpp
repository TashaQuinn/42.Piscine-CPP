#include "Point.hpp"

bool bsp(Point const a, Point const b, Point const c, Point const point) {
	
	Fixed pl_a;
	Fixed pl_b;
	Fixed pl_c;

	pl_a = (a.getX() - point.getX()) * (b.getY() - a.getY()) - (b.getX() - a.getX()) * (a.getY() - point.getY());
	pl_b = (b.getX() - point.getX()) * (c.getY() - b.getY()) - (c.getX() - b.getX()) * (b.getY() - point.getY());
	pl_c = (c.getX() - point.getX()) * (a.getY() - c.getY()) - (a.getX() - c.getX()) * (c.getY() - point.getY());

	if ((pl_a > 0 && pl_b > 0 && pl_c > 0) || (pl_a < 0 && pl_b < 0 && pl_c < 0))
		return true;
	return false;
	
}
